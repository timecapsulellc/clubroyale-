final ledgerProvider = FutureProvider.family<GameRoom?, String>((ref, gameId) {
  final ledgerService = ref.watch(ledgerServiceProvider);
  return ledgerService.getGame(gameId);
});

class LedgerScreen extends ConsumerStatefulWidget {
  final String gameId;

  const LedgerScreen({super.key, required this.gameId});

  @override
  ConsumerState<LedgerScreen> createState() => _LedgerScreenState();
}

class _LedgerScreenState extends ConsumerState<LedgerScreen> {
  final GlobalKey _receiptKey = GlobalKey();
  bool _isGeneratingImage = false;

  /// Calculate settlements from game scores
  List<Transaction> _calculateSettlements(GameRoom game) {
    final settlementService = SettlementService();
    // Make a copy of scores to avoid mutating the original
    final scoresCopy = Map<String, int>.from(game.scores);
    return settlementService.calculateDebts(scoresCopy, game.config.pointValue);
  }

  /// Get player name from ID
  String _getPlayerName(GameRoom game, String playerId) {
    final player = game.players.firstWhere(
      (p) => p.id == playerId,
      orElse: () => Player(id: playerId, name: 'Unknown'),
    );
    return player.profile?.displayName ?? player.name;
  }

