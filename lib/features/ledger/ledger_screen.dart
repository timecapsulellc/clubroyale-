}

class _InfoChip extends StatelessWidget {
  final IconData icon;
  final String label;

  const _InfoChip({required this.icon, required this.label});

  @override
  Widget build(BuildContext context) {
    return Row(
      mainAxisSize: MainAxisSize.min,
      children: [
        Icon(icon, size: 16, color: Theme.of(context).colorScheme.onSurfaceVariant),
        const SizedBox(width: 4),
        Text(label, style: Theme.of(context).textTheme.bodySmall),
      ],
    );
  }
}

class _TransactionTile extends StatelessWidget {
  final String fromName;
  final String toName;
  final double amount;

  const _TransactionTile({
    required this.fromName,
    required this.toName,
    required this.amount,
  });

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final colorScheme = theme.colorScheme;

    return Container(
      margin: const EdgeInsets.only(bottom: 8),
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: colorScheme.primaryContainer.withOpacity(0.3),
        borderRadius: BorderRadius.circular(12),
        border: Border.all(color: colorScheme.outline.withOpacity(0.2)),
      ),
      child: Row(
        children: [
          // From avatar
          CircleAvatar(
            radius: 16,
            backgroundColor: Colors.red.withOpacity(0.2),
            child: Text(
              fromName[0].toUpperCase(),
              style: const TextStyle(color: Colors.red, fontWeight: FontWeight.bold, fontSize: 12),
            ),
          ),
          const SizedBox(width: 8),
          // From name
          Expanded(
            child: Text(fromName, style: theme.textTheme.bodyMedium),
          ),
          // Arrow with amount
          Container(
            padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 4),
            decoration: BoxDecoration(
              color: colorScheme.surface,
              borderRadius: BorderRadius.circular(16),
            ),
            child: Row(
              mainAxisSize: MainAxisSize.min,
              children: [
                Text(
                  '${amount.toInt()}',
                  style: theme.textTheme.titleMedium?.copyWith(
                    fontWeight: FontWeight.bold,
                    color: colorScheme.primary,
                  ),
                ),
                const SizedBox(width: 4),
                Icon(Icons.arrow_forward, size: 16, color: colorScheme.primary),
              ],
            ),
          ),
          const SizedBox(width: 8),
          // To name
          Expanded(
            child: Text(toName, style: theme.textTheme.bodyMedium, textAlign: TextAlign.right),
          ),
          const SizedBox(width: 8),
          // To avatar
          CircleAvatar(
            radius: 16,
            backgroundColor: Colors.green.withOpacity(0.2),
            child: Text(
              toName[0].toUpperCase(),
              style: const TextStyle(color: Colors.green, fontWeight: FontWeight.bold, fontSize: 12),
            ),
          ),
        ],
      ),
    );
  }
}
